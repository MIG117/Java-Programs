// 1. Write a program to find area and perimeter of a circle.
public class Perimeter {
    public static void main(String args[]) {
        int r = 5;
        double p;
        System.out.println("Radius is: " + r);
        p = 2 * 3.14 * r;
        System.out.printf("Perimeter of circle is: %.3f", p);
    }
}
