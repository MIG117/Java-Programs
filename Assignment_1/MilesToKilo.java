// 6. Write a program to convert miles to kilometres.
public class MilesToKilo {
    public static void main(String args[]) {
        double ml, kl;
        ml = 65;
        System.out.println("Enter Miles: " + ml);
        kl = ml * 1.60934;
        System.out.println("Miles to Kilometers is: " + kl);
    }
}
